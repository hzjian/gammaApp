package com.cellinfo.controller.entity;

public class GAFilter {
	
	private String attrGuid;

	private String attrType;
	
	private String minValue;

	private String maxValue;
	
	private String enumStr;

	/**
	 * @return the attrGuid
	 */
	public String getAttrGuid() {
		return attrGuid;
	}

	/**
	 * @param attrGuid the attrGuid to set
	 */
	public void setAttrGuid(String attrGuid) {
		this.attrGuid = attrGuid;
	}

	/**
	 * @return the attrType
	 */
	public String getAttrType() {
		return attrType;
	}

	/**
	 * @param attrType the attrType to set
	 */
	public void setAttrType(String attrType) {
		this.attrType = attrType;
	}

	/**
	 * @return the minValue
	 */
	public String getMinValue() {
		return minValue;
	}

	/**
	 * @param minValue the minValue to set
	 */
	public void setMinValue(String minValue) {
		this.minValue = minValue;
	}

	/**
	 * @return the maxValue
	 */
	public String getMaxValue() {
		return maxValue;
	}

	/**
	 * @param maxValue the maxValue to set
	 */
	public void setMaxValue(String maxValue) {
		this.maxValue = maxValue;
	}

	/**
	 * @return the enumStr
	 */
	public String getEnumStr() {
		return enumStr;
	}

	/**
	 * @param enumStr the enumStr to set
	 */
	public void setEnumStr(String enumStr) {
		this.enumStr = enumStr;
	}
	
	
	
}
