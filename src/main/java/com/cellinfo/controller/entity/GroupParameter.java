package com.cellinfo.controller.entity;

public class GroupParameter {

	private String groupGuid;

	private String groupAddress;

	private String groupCode;

	private String groupEmail;

	private String groupName;

	private String groupPhone;

	private String groupPic;

	private Integer groupStatus;
	
	private String groupService;

	/**
	 * @return the groupGuid
	 */
	public String getGroupGuid() {
		return groupGuid;
	}

	/**
	 * @param groupGuid the groupGuid to set
	 */
	public void setGroupGuid(String groupGuid) {
		this.groupGuid = groupGuid;
	}

	/**
	 * @return the groupAddress
	 */
	public String getGroupAddress() {
		return groupAddress;
	}

	/**
	 * @param groupAddress the groupAddress to set
	 */
	public void setGroupAddress(String groupAddress) {
		this.groupAddress = groupAddress;
	}

	/**
	 * @return the groupCode
	 */
	public String getGroupCode() {
		return groupCode;
	}

	/**
	 * @param groupCode the groupCode to set
	 */
	public void setGroupCode(String groupCode) {
		this.groupCode = groupCode;
	}

	/**
	 * @return the groupEmail
	 */
	public String getGroupEmail() {
		return groupEmail;
	}

	/**
	 * @param groupEmail the groupEmail to set
	 */
	public void setGroupEmail(String groupEmail) {
		this.groupEmail = groupEmail;
	}

	/**
	 * @return the groupName
	 */
	public String getGroupName() {
		return groupName;
	}

	/**
	 * @param groupName the groupName to set
	 */
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}

	/**
	 * @return the groupPhone
	 */
	public String getGroupPhone() {
		return groupPhone;
	}

	/**
	 * @param groupPhone the groupPhone to set
	 */
	public void setGroupPhone(String groupPhone) {
		this.groupPhone = groupPhone;
	}

	/**
	 * @return the groupPic
	 */
	public String getGroupPic() {
		return groupPic;
	}

	/**
	 * @param groupPic the groupPic to set
	 */
	public void setGroupPic(String groupPic) {
		this.groupPic = groupPic;
	}

	/**
	 * @return the groupStatus
	 */
	public Integer getGroupStatus() {
		return groupStatus;
	}

	/**
	 * @param groupStatus the groupStatus to set
	 */
	public void setGroupStatus(Integer groupStatus) {
		this.groupStatus = groupStatus;
	}

	/**
	 * @return the groupService
	 */
	public String getGroupService() {
		return groupService;
	}

	/**
	 * @param groupService the groupService to set
	 */
	public void setGroupService(String groupService) {
		this.groupService = groupService;
	}
	
	
}
